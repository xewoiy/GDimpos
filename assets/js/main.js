function frameget_minfps_fun(){
  if (document.getElementById("frameget_minfps_div").style.display === "none") {
    document.getElementById("frameget_minfps_div").style.display = "block";
  } else {
    document.getElementById("frameget_minfps_div").style.display = "none";
  }
};

